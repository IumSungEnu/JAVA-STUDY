package exam;

public class Ex04 {
    public static void main(String[] args){
        long num1 = 10000000000L;
        //num1 -> int num1 -> long num1
        //int를 기준으로 용량이 많으면 long으로 선언하여 바꿔준다.

        byte num2 = 100;   //num2 -> int num2 -> byte num2
        //int를 기준으로 용량이 적으면 byte로 선언하여 바꿔준다.
    }
}
