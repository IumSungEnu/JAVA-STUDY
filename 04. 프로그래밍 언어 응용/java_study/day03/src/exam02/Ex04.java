package exam02;

public class Ex04 {
    //static으로 선언하여 값을 입력하지 않아도 기본값이 나오도록 할수 있다.
    static  int num;
    static double num2;
    static boolean bool;
    static char ch;
    static String str;
    public static void main(String[] args){

        System.out.println(num);
        System.out.println(num2);
        System.out.println(bool);
        System.out.println(ch);
        System.out.println(str);
    }
}
